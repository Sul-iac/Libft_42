#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *str)
{
	int	i;
	int	len;
	char	*dup;
	
	i = 0;
	len = ft_strlen(str);
	dup = (char *)malloc(sizeof(char) * len + 1);
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}

